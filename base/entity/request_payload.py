


class RequestPayload:

    source_ip = ""
    destination_ip = ""
    destination_port = ""
    request_date = ""
    request_time = ""
    request_method = ""
    request = ""
    request_size = ""

    def __init__(self):
        pass


    def getSourceIp(self):
        return self.source_ip

    def setSourceIp(ip):
        source_ip = ip

    def getRequest():
        return request

    def setRequest(request):
        request = request

    def getDestinationIp():
        return destination_ip

    def setDestinationIp(ip):
        destination_ip = ip

    def getDestinationPort():
        return destination_port

    def setDestinationPort(port):
        destination_port = port
    
    def getRequestDate():
        return request_date

    def setRequestDate(reqDate):
        request_date = reqDate
    
    def getRequestTime():
        return request_time

    def setRequestTime(reqTime):
        request_time = reqTime

    def getRequestMethod():
        return request_method

    def setRequestMethod(reqMethod):
        request_method = reqMethod

    def getRequestSize():
        return request_size

    def setRequestSize(reqSize):
        request_size = reqSize
    